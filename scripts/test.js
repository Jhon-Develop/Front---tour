function showAllCards() {
  if (!cardsDisplayed) {
    const container = document.querySelector('.P_section_highlights');
    ApiTours.forEach(card => {
      const cardElement = createCard(card);
      container.appendChild(cardElement);
    });
    document.querySelectorAll('.cards_highlights').forEach(card => {
      card.style.display = 'block';
    });
    cardsDisplayed = true; // Marcar que las cartas ya se han mostrado
    document.querySelector('.see_more').disabled = true; // Deshabilitar el botón "Ver más"
  }
}

document.querySelector('.see_more').addEventListener('click', showAllCards);